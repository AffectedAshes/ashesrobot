// Function called when the "!randrunner" command is issued
function randrunnerCommand(target, username, client) {
    const firstName = getRandomNamePart(runner_list, 0); // Get random first name part
    const lastName = getRandomNamePart(runner_list, 1); // Get random last name part
  
    const combinedName = `${firstName}${lastName}`;
    client.say(target, `@${username} Your random runner name is: ${combinedName}`);
  }
  
  // Helper function to get a random name part from the list
  function getRandomNamePart(nameList, partIndex) {
    const filteredList = nameList.map((name) => name.split(' ')[partIndex]);
    const randomIndex = Math.floor(Math.random() * filteredList.length);
    return filteredList[randomIndex];
  }
  
